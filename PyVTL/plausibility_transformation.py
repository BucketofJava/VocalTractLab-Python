import os
import joblib
from scipy.spatial.distance import cosine

model = joblib.load( os.path.join( os.path.dirname(__file__),'Models/PCA_nc_10.joblib') )
print( os.path.dirname(__file__) )
#model = joblib.load( model_path )

def encode_decode( features ):
	encode = model.transform( features )
	decode = model.inverse_transform( encode )
	return [ features[0], encode[0], decode[0], cosine( features, decode) ]